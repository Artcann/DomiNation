package org.example.core;

// Tile Class is created for polymorphism

public class Tile {

    protected int x;
    protected int y;

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

}
